wlist = ['밥','삶','길','죽','꿈','차','떡','복','말']
print('wlist[:] = ',wlist[:])
print('wlist[::] = ',wlist[::])
print('wlist[::-1] = ' ,wlist[::-1])
# 순방향
print(wlist[::3])
print(wlist[1::3])
print(wlist[2::3])
#역방향
print(wlist[::-2])
print(wlist[-1:-8:-3])
#순방향과 역방향, 다양한 첨자
print(wlist[1:-1:])
print(wlist[-2:-9:-3])
